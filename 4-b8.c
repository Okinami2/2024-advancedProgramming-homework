   /***************************************************************************
	 函数名称：
	 功    能：将整数n分解后输出
	 输入参数：
	 返 回 值：
	 说    明：1、函数名、形参、返回类型均不准动
			   2、不允许使用64位整数
   ***************************************************************************/
void convert(int n)
{

	if (n / 10 == 0) {
		if (n < 0) {
			printf("%c", 48 - n);
			printf("%c%c", 32, 45);
		}
		else {
			printf("%c", 48 + n);
		}
	}
	else {
		printf("%c%c", 48 + (n > 0 ? (n % 10) : -(n % 10)), 32);
		convert(n / 10);
	}
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：main函数不准动
***************************************************************************/
int main()
{
	int n;
	printf("请输入一个整数\n");
	scanf("%d", &n);

	convert(n);
	printf("\n");

	return 0;
}
